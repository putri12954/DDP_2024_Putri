import math

#deklarasi
def l_persegi(sisi):
    hitung = sisi * sisi
    print(f"Luas persegi adalah {hitung}")
    
def l_persegi_pajang(p, l):
    hitung = p * l
    print(f"Luas persegi pannjang adalah {hitung}")
    
def l_segi_tiga(alas, tinggi):
    hitung = 1/2 * alas * tinggi
    print(f"Luas segitiga adalah {hitung}")
    
def l_jajar_genjang(alas, tinggi):
    hitung = alas * tinggi
    print(f"Luas jajar genjang adalah {hitung}")
    
def l_lingkaran(r):
    hitung = r * 3.14 * r
    print(f"Luas lingkaran adalah {hitung}")
    
    